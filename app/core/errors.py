"""Иерархия исключений приложения.

Все бизнес-ошибки наследуются от :class:`AppError`. Это позволяет в UI
ловить «свои» исключения и показывать пользователю осмысленное сообщение,
а ``Exception`` без типа считать программной ошибкой и логировать с трейсом.
"""

from __future__ import annotations


class AppError(Exception):
    """Базовая ошибка приложения. Сообщение предназначено для показа пользователю."""


class ConfigError(AppError):
    """Некорректная или отсутствующая конфигурация."""


class AuthenticationError(AppError):
    """Не удалось аутентифицировать пользователя (неверный логин/пароль, блокировка)."""


class PermissionDenied(AppError):
    """У текущего пользователя нет требуемого права на выполнение операции."""


class ValidationError(AppError):
    """Невалидные входные данные (нарушены инварианты доменной модели)."""


class NotFound(AppError):
    """Запрошенная сущность не найдена."""


class ConflictError(AppError):
    """Конфликт состояния (например, нарушение уникальности на уровне домена)."""
